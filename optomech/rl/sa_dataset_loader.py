"""
SA Dataset Loader for TD3 Replay Buffer

This module provides functionality to pre-load a TD3 replay buffer with data from
SA (Simulated Annealing) datasets generated by build_sa_dataset.py.

The loader:
1. Reads HDF5 batch files from an SA dataset directory
2. Groups transitions by episode using episode_ids and episode_steps
3. Creates sequences of length tbptt_seq_len for TBPTT training
4. Computes initial hidden states by running the first observation through the models
5. Supports using either 'perfect_actions' or 'sa_actions' as the action target

Usage:
    from optomech.rl.sa_dataset_loader import load_sa_dataset_to_buffer
    
    sequences_added = load_sa_dataset_to_buffer(
        replay_buffer=rb,
        dataset_path="datasets/sa_dataset_1m",
        actor_model=actor,
        qf1_model=qf1,
        qf2_model=qf2,
        sequence_length=16,
        action_type="perfect_actions",  # or "sa_actions"
        device=device,
        max_sequences=1000,  # Optional: limit number of sequences
        verbose=True
    )
"""

import os
import sys
from pathlib import Path
from typing import Optional, List, Dict, Tuple, Union
from collections import defaultdict

import numpy as np
import torch
import h5py


def load_sa_dataset_to_buffer(
    replay_buffer,
    dataset_path: Union[str, Path],
    actor_model: torch.nn.Module,
    qf1_model: torch.nn.Module,
    qf2_model: torch.nn.Module,
    sequence_length: int,
    action_type: str = "perfect_actions",
    device: Optional[torch.device] = None,
    max_sequences: Optional[int] = None,
    max_episodes: Optional[int] = None,
    verbose: bool = True
) -> int:
    """
    Load SA dataset into a TD3 replay buffer.
    
    Args:
        replay_buffer: TD3ReplayBufferLSTM instance to populate
        dataset_path: Path to directory containing SA dataset batch files (*.h5)
        actor_model: Actor model for computing initial hidden states
        qf1_model: First critic model for computing initial hidden states
        qf2_model: Second critic model for computing initial hidden states
        sequence_length: Length of sequences for TBPTT (tbptt_seq_len)
        action_type: Which action field to use - "perfect_actions" or "sa_actions"
        device: Device to run models on (if None, uses model's current device)
        max_sequences: Maximum number of sequences to load (None = no limit)
        max_episodes: Maximum number of episodes to load (None = no limit)
        verbose: Whether to print progress information
        
    Returns:
        Number of sequences added to the replay buffer
    """
    dataset_path = Path(dataset_path)
    
    if not dataset_path.exists():
        raise FileNotFoundError(f"Dataset path does not exist: {dataset_path}")
    
    # Find all batch files
    batch_files = sorted(dataset_path.glob("batch_*.h5"))
    if not batch_files:
        raise FileNotFoundError(f"No batch files found in {dataset_path}")
    
    if verbose:
        print(f"📂 Loading SA dataset from: {dataset_path}")
        print(f"   Found {len(batch_files)} batch files")
        print(f"   Action type: {action_type}")
        print(f"   Sequence length: {sequence_length}")
        if max_sequences:
            print(f"   Max sequences: {max_sequences:,}")
        if max_episodes:
            print(f"   Max episodes: {max_episodes:,}")
    
    # Validate action_type
    valid_action_types = ["perfect_actions", "sa_actions", "perfect_incremental_actions", "sa_incremental_actions"]
    if action_type not in valid_action_types:
        raise ValueError(f"action_type must be one of {valid_action_types}, got {action_type}")
    
    # Load and merge all batch files into episode-grouped data
    all_episodes = defaultdict(list)
    total_samples_loaded = 0
    
    import time
    load_start_time = time.time()
    
    # Estimate how many samples we need to load (with 2x safety margin for episode boundaries)
    # Each sequence needs `sequence_length` samples, but episodes may not divide evenly
    estimated_samples_needed = None
    if max_sequences is not None:
        estimated_samples_needed = max_sequences * sequence_length * 2  # 2x margin for episode boundaries
        if verbose:
            print(f"   Estimated samples needed: ~{estimated_samples_needed:,} (with 2x margin)")
    
    if verbose:
        print(f"\n   Phase 1/3: Loading batch files from disk...")
    
    early_stopped = False
    for batch_idx, batch_file in enumerate(batch_files):
        # Early stop if we have enough samples
        if estimated_samples_needed is not None and total_samples_loaded >= estimated_samples_needed:
            early_stopped = True
            if verbose:
                elapsed = time.time() - load_start_time
                print(f"      [EARLY STOP] Loaded enough samples ({total_samples_loaded:,}) after {batch_idx:,} files in {elapsed:.1f}s")
            break
        
        try:
            with h5py.File(batch_file, 'r') as f:
                # Load arrays
                observations = f['observations'][:]
                next_observations = f['next_observations'][:]
                actions = f[action_type][:]
                rewards = f['rewards'][:]
                episode_ids = f['episode_ids'][:]
                episode_steps = f['episode_steps'][:]
                
                # Debug: print shapes from first file
                if batch_idx == 0 and verbose:
                    print(f"      [DEBUG] Raw shapes from HDF5:")
                    print(f"        observations: {observations.shape}, dtype={observations.dtype}")
                    print(f"        actions: {actions.shape}, dtype={actions.dtype}")
                    print(f"        rewards: {rewards.shape}, dtype={rewards.dtype}")
                
                # Group by episode
                for i in range(len(observations)):
                    ep_id = episode_ids[i]
                    if isinstance(ep_id, bytes):
                        ep_id = ep_id.decode('utf-8')
                    
                    all_episodes[ep_id].append({
                        'step': episode_steps[i],
                        'observation': observations[i],
                        'next_observation': next_observations[i],
                        'action': actions[i],
                        'reward': rewards[i]
                    })
                    total_samples_loaded += 1
                    
        except Exception as e:
            if verbose:
                print(f"   ⚠️ Error loading {batch_file.name}: {e}")
            continue
        
        # Progress update every 10% of target (if early stopping) or total files
        if estimated_samples_needed is not None:
            # Progress based on samples loaded vs needed - update every ~10%
            progress_interval = max(1, estimated_samples_needed // 10)
            current_bucket = total_samples_loaded // progress_interval
            prev_bucket = (total_samples_loaded - 10) // progress_interval  # Approx previous (assuming ~10 samples per file)
            if verbose and (current_bucket > prev_bucket or (batch_idx + 1) == len(batch_files)):
                elapsed = time.time() - load_start_time
                pct = min(100.0, 100 * total_samples_loaded / estimated_samples_needed)
                print(f"      [{pct:5.1f}%] {batch_idx + 1:,} files | {total_samples_loaded:,}/{estimated_samples_needed:,} samples | {len(all_episodes):,} episodes | {elapsed:.1f}s")
        else:
            # Progress based on files processed
            if verbose and ((batch_idx + 1) % max(1, len(batch_files) // 10) == 0 or (batch_idx + 1) == len(batch_files)):
                elapsed = time.time() - load_start_time
                pct = 100 * (batch_idx + 1) / len(batch_files)
                print(f"      [{pct:5.1f}%] {batch_idx + 1:,}/{len(batch_files):,} files | {total_samples_loaded:,} samples | {len(all_episodes):,} episodes | {elapsed:.1f}s")
    
    if verbose:
        load_elapsed = time.time() - load_start_time
        if early_stopped:
            print(f"   ✓ Loaded {total_samples_loaded:,} samples from {len(all_episodes):,} episodes in {load_elapsed:.1f}s (early stopped)")
        else:
            print(f"   ✓ Loaded {total_samples_loaded:,} samples from {len(all_episodes):,} episodes in {load_elapsed:.1f}s")
        print(f"\n   Phase 2/3: Sorting and validating episodes...")
    
    # Sort each episode by step number
    sort_start_time = time.time()
    for ep_id in all_episodes:
        all_episodes[ep_id].sort(key=lambda x: x['step'])
    
    # Filter episodes that are long enough for at least one sequence
    valid_episodes = {
        ep_id: transitions 
        for ep_id, transitions in all_episodes.items() 
        if len(transitions) >= sequence_length
    }
    
    if verbose:
        sort_elapsed = time.time() - sort_start_time
        skipped_episodes = len(all_episodes) - len(valid_episodes)
        if skipped_episodes > 0:
            print(f"   ✓ {len(valid_episodes):,} episodes valid (skipped {skipped_episodes:,} too short) in {sort_elapsed:.1f}s")
        else:
            print(f"   ✓ All {len(valid_episodes):,} episodes valid in {sort_elapsed:.1f}s")
    
    if not valid_episodes:
        print("   ⚠️ No episodes with enough steps for the requested sequence length")
        return 0
    
    # Limit episodes if requested
    episode_ids = list(valid_episodes.keys())
    if max_episodes is not None and len(episode_ids) > max_episodes:
        episode_ids = episode_ids[:max_episodes]
        if verbose:
            print(f"   Limiting to first {max_episodes:,} episodes")
    
    # Estimate total sequences
    estimated_sequences = sum(
        (len(valid_episodes[ep_id]) - sequence_length + 1) // sequence_length
        for ep_id in episode_ids
    )
    if max_sequences is not None:
        estimated_sequences = min(estimated_sequences, max_sequences)
    
    if verbose:
        print(f"\n   Phase 3/3: Building {estimated_sequences:,} sequences...")
    
    # Put models in eval mode
    actor_model.eval()
    qf1_model.eval()
    qf2_model.eval()
    
    # Process episodes into sequences
    sequences_added = 0
    seq_start_time = time.time()
    
    with torch.no_grad():
        for ep_idx, ep_id in enumerate(episode_ids):
            episode = valid_episodes[ep_id]
            
            # Check if we've hit the sequence limit
            if max_sequences is not None and sequences_added >= max_sequences:
                break
            
            # Split episode into non-overlapping sequences
            for seq_start in range(0, len(episode) - sequence_length + 1, sequence_length):
                # Check sequence limit again
                if max_sequences is not None and sequences_added >= max_sequences:
                    break
                
                seq_end = seq_start + sequence_length
                sequence_data = episode[seq_start:seq_end]
                
                # Extract sequences
                obs_seq = [d['observation'] for d in sequence_data]
                next_obs_seq = [d['next_observation'] for d in sequence_data]
                action_seq = [d['action'] for d in sequence_data]
                reward_seq = [d['reward'] for d in sequence_data]
                
                # Convert observations to float32 and normalize if needed
                # SA dataset stores uint16, we need float32 in [0,1] range
                # Also add batch dimension to match SyncVectorEnv output: (1, C, H, W)
                obs_seq = [np.expand_dims(obs.astype(np.float32) / 65535.0, axis=0) for obs in obs_seq]
                next_obs_seq = [np.expand_dims(obs.astype(np.float32) / 65535.0, axis=0) for obs in next_obs_seq]
                
                # Add batch dimension to actions to match SyncVectorEnv output: (1, action_dim)
                action_seq = [np.expand_dims(a, axis=0) for a in action_seq]
                
                # Create prior action and reward sequences
                # For first step of sequence, use zeros (with batch dimension for actions)
                # Rewards should be shape (1,) to match SyncVectorEnv output
                prior_action_seq = [np.zeros_like(action_seq[0])] + action_seq[:-1]
                prior_reward_seq = [np.array([0.0], dtype=np.float32)] + [
                    np.array([r], dtype=np.float32) for r in reward_seq[:-1]
                ]
                
                # Ensure rewards are arrays with shape (1,) - to match SyncVectorEnv output
                reward_seq = [np.array([r], dtype=np.float32) for r in reward_seq]
                
                # Create done flags (all False for now - SA doesn't track terminations)
                # Shape: (1,) to match SyncVectorEnv
                done_seq = [np.array([False], dtype=np.float32) for _ in range(sequence_length)]
                
                # Compute initial hidden states using the first observation
                first_obs = torch.from_numpy(obs_seq[0]).unsqueeze(0)  # Add batch dim
                first_prior_action = torch.from_numpy(prior_action_seq[0]).unsqueeze(0)
                first_prior_reward = torch.from_numpy(prior_reward_seq[0]).unsqueeze(0)
                
                if device is not None:
                    first_obs = first_obs.to(device, dtype=torch.float32)
                    first_prior_action = first_prior_action.to(device, dtype=torch.float32)
                    first_prior_reward = first_prior_reward.to(device, dtype=torch.float32)
                
                # Get zero hidden states as starting point
                # Model expects [num_layers, 1, hidden_dim] (3D with batch dim)
                # get_zero_hidden() returns [num_layers, hidden_dim] (2D)
                # So we need to unsqueeze(1) to add the batch dimension
                actor_h, actor_c = actor_model.get_zero_hidden()
                initial_actor_hidden = (actor_h.unsqueeze(1), actor_c.unsqueeze(1))
                
                qf1_h, qf1_c = qf1_model.get_zero_hidden()
                initial_qf1_hidden = (qf1_h.unsqueeze(1), qf1_c.unsqueeze(1))
                
                qf2_h, qf2_c = qf2_model.get_zero_hidden()
                initial_qf2_hidden = (qf2_h.unsqueeze(1), qf2_c.unsqueeze(1))
                
                # Debug: verify hidden state shape on first sequence
                if sequences_added == 0 and verbose:
                    print(f"   Hidden state shapes (after unsqueeze for batch dim):")
                    print(f"      Actor: h={initial_actor_hidden[0].shape}, c={initial_actor_hidden[1].shape}")
                    print(f"      Expected: [num_layers={actor_model.lstm_num_layers}, 1, hidden_dim={actor_model.lstm_hidden_dim}]")
                    print(f"      Qf1: h={initial_qf1_hidden[0].shape}")
                    print(f"      Qf2: h={initial_qf2_hidden[0].shape}")
                
                # Optionally: Run the first observation through models to get
                # "warmed up" hidden states. For now, we use zero hidden states
                # which is simpler and consistent with how online data is collected.
                # If you want warmed-up states, uncomment the following:
                #
                # _, initial_actor_hidden = actor_model(
                #     first_obs, first_prior_action, first_prior_reward, initial_actor_hidden
                # )
                # first_action = torch.from_numpy(action_seq[0]).unsqueeze(0).to(device, dtype=torch.float32)
                # _, initial_qf1_hidden = qf1_model(
                #     first_obs, first_action, first_prior_action, first_prior_reward, initial_qf1_hidden
                # )
                # _, initial_qf2_hidden = qf2_model(
                #     first_obs, first_action, first_prior_action, first_prior_reward, initial_qf2_hidden
                # )
                
                # Move hidden states to device if specified
                if device is not None:
                    initial_actor_hidden = (
                        initial_actor_hidden[0].to(device),
                        initial_actor_hidden[1].to(device)
                    )
                    initial_qf1_hidden = (
                        initial_qf1_hidden[0].to(device),
                        initial_qf1_hidden[1].to(device)
                    )
                    initial_qf2_hidden = (
                        initial_qf2_hidden[0].to(device),
                        initial_qf2_hidden[1].to(device)
                    )
                
                # Add sequence to replay buffer
                try:
                    replay_buffer.push(
                        initial_actor_hidden=initial_actor_hidden,
                        initial_qf1_hidden=initial_qf1_hidden,
                        initial_qf2_hidden=initial_qf2_hidden,
                        observation_sequence=obs_seq,
                        action_sequence=action_seq,
                        prior_action_sequence=prior_action_seq,
                        reward_sequence=reward_seq,
                        prior_reward_sequence=prior_reward_seq,
                        next_observation_sequence=next_obs_seq,
                        done_sequence=done_seq,
                        metadata={'episode_id': ep_id, 'sequence_start': seq_start, 'source': 'sa_dataset'}
                    )
                    sequences_added += 1
                except Exception as e:
                    if verbose and sequences_added == 0:
                        print(f"   ⚠️ Error adding first sequence: {e}")
                        print(f"      obs_seq[0] shape: {obs_seq[0].shape}")
                        print(f"      action_seq[0] shape: {action_seq[0].shape}")
                        print(f"      reward_seq[0] shape: {reward_seq[0].shape}")
                    continue
            
            # Progress update
            if verbose and ((ep_idx + 1) % max(1, len(episode_ids) // 10) == 0 or (ep_idx + 1) == len(episode_ids)):
                elapsed = time.time() - seq_start_time
                pct = 100 * (ep_idx + 1) / len(episode_ids)
                seq_rate = sequences_added / elapsed if elapsed > 0 else 0
                print(f"      [{pct:5.1f}%] {ep_idx + 1:,}/{len(episode_ids):,} episodes | {sequences_added:,} sequences | {seq_rate:.0f} seq/s | {elapsed:.1f}s")
    
    if verbose:
        total_elapsed = time.time() - load_start_time
        print(f"\n✅ Added {sequences_added:,} sequences to replay buffer in {total_elapsed:.1f}s")
        print(f"   Buffer now contains {len(replay_buffer):,} sequences")
    
    return sequences_added


def get_sa_dataset_info(dataset_path: Union[str, Path]) -> Dict:
    """
    Get information about an SA dataset without loading all the data.
    
    Args:
        dataset_path: Path to directory containing SA dataset batch files
        
    Returns:
        Dictionary with dataset statistics
    """
    dataset_path = Path(dataset_path)
    
    if not dataset_path.exists():
        raise FileNotFoundError(f"Dataset path does not exist: {dataset_path}")
    
    batch_files = sorted(dataset_path.glob("batch_*.h5"))
    if not batch_files:
        raise FileNotFoundError(f"No batch files found in {dataset_path}")
    
    info = {
        'path': str(dataset_path),
        'num_batch_files': len(batch_files),
        'total_samples': 0,
        'unique_episodes': set(),
        'observation_shape': None,
        'action_shape': None,
        'attributes': {}
    }
    
    for batch_file in batch_files:
        try:
            with h5py.File(batch_file, 'r') as f:
                info['total_samples'] += f['observations'].shape[0]
                
                if info['observation_shape'] is None:
                    info['observation_shape'] = f['observations'].shape[1:]
                    info['action_shape'] = f['perfect_actions'].shape[1:]
                    info['attributes'] = dict(f.attrs)
                
                episode_ids = f['episode_ids'][:]
                for ep_id in episode_ids:
                    if isinstance(ep_id, bytes):
                        ep_id = ep_id.decode('utf-8')
                    info['unique_episodes'].add(ep_id)
                    
        except Exception as e:
            print(f"Warning: Error reading {batch_file}: {e}")
            continue
    
    info['num_episodes'] = len(info['unique_episodes'])
    info['unique_episodes'] = None  # Don't return the full set
    
    return info


